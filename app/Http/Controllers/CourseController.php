    public function coursesPaginationData()
    {
        return Course::query()
        ->when(Request::input("search"), function ($query, $search) {
            $query->where("name", "like", '%'.$search.'%')
                ->orWhere("description", "like", '%'.$search.'%');
        })
        // ->with(['role', 'subscription'])
        ->paginate(10)
        ->through(fn($bundle) => [
            "id" => $bundle->id,
            "name" => $bundle->name,
            "description" => $bundle->description,
            "image" => $bundle->image,
            "space" => $bundle->space,
        ]);

    }
